package hwms.entity;

public class Course {
	int cour_Num;
	String cour_Name;
	int cour_Credit;
	String cour_ID;
	int cour_Count;
	String cour_t_ID;
	
	public int getCour_Num() {
		return cour_Num;
	}
	public void setCour_Num(int cour_Num) {
		this.cour_Num = cour_Num;
	}
	public String getCour_Name() {
		return cour_Name;
	}
	public void setCour_Name(String cour_Name) {
		this.cour_Name = cour_Name;
	}
	public int getCour_Credit() {
		return cour_Credit;
	}
	public void setCour_Credit(int cour_Credit) {
		this.cour_Credit = cour_Credit;
	}
	public String getCour_ID() {
		return cour_ID;
	}
	public void setCour_ID(String cour_ID) {
		this.cour_ID = cour_ID;
	}
	public int getCour_Count() {
		return cour_Count;
	}
	public void setCour_Count(int cour_count) {
		this.cour_Count = cour_count;
	}
	public String getCour_t_ID() {
		return cour_t_ID;
	}
	public void setCour_t_ID(String cour_t_ID) {
		this.cour_t_ID = cour_t_ID;
	}
}
