package hwms.entity;

public class StudentCourse {
	int sc_Num;
	String sc_s_Account;
	String sc_cour_ID;
	public int getSc_Num() {
		return sc_Num;
	}
	public void setSc_Num(int sc_Num) {
		this.sc_Num = sc_Num;
	}
	public String getSc_s_Account() {
		return sc_s_Account;
	}
	public void setSc_s_Account(String sc_s_Account) {
		this.sc_s_Account = sc_s_Account;
	}
	public String getSc_cour_ID() {
		return sc_cour_ID;
	}
	public void setSc_cour_ID(String sc_cour_ID) {
		this.sc_cour_ID = sc_cour_ID;
	}
}
