package hwms.entity;

public class Invitation {
	int in_Num;
	String in_a_Account;
	String in_cour_ID;
	String in_Accept;
	public int getIn_Num() {
		return in_Num;
	}
	public void setIn_Num(int in_Num) {
		this.in_Num = in_Num;
	}
	public String getIn_a_Account() {
		return in_a_Account;
	}
	public void setIn_a_Account(String in_a_Account) {
		this.in_a_Account = in_a_Account;
	}
	public String getIn_cour_ID() {
		return in_cour_ID;
	}
	public void setIn_cour_ID(String in_cour_ID) {
		this.in_cour_ID = in_cour_ID;
	}
	public String getIn_Accept() {
		return in_Accept;
	}
	public void setIn_Accept(String in_Accept) {
		this.in_Accept = in_Accept;
	}
}
